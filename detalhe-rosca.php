<div class="info-card">
<h2>Descrição</h2>
<p>A rosca direta é um exercício clássico para fortalecimento dos bíceps, muito utilizado em treinos de membros superiores.</p>
</div>

<div class="info-card">
<h2>Músculos trabalhados</h2>
<ul>
<li>Bíceps braquial</li>
<li>Braquial</li>
<li>Braquiorradial</li>
</ul>
</div>

<div class="info-card">
<h2>Passo a passo</h2>
<ol>
<li>Segure a barra ou halteres com as palmas voltadas para frente.</li>
<li>Mantenha os cotovelos próximos ao corpo.</li>
<li>Flexione os braços levantando o peso até a altura do peito.</li>
<li>Contraia bem os bíceps no topo do movimento.</li>
<li>Desça lentamente controlando o peso.</li>
</ol>
</div>

<div class="info-card">
<h2>Prescrição</h2>
<p><strong>Séries:</strong> 3</p>
<p><strong>Repetições:</strong> 10-12</p>
<p><strong>Descanso:</strong> 30-45s</p>
</div>

