<div class="info-card">
<h2>Descrição</h2>
<p>O Leg Press é um exercício de musculação muito utilizado para fortalecer os músculos das pernas. Ele é realizado em uma máquina onde o praticante empurra uma plataforma com os pés, trabalhando principalmente quadríceps, glúteos e posteriores de coxa.</p>
</div>

<div class="info-card">
<h2>Músculos trabalhados</h2>
<ul>
<li>Quadríceps</li>
<li>Glúteos</li>
<li>Posterior da coxa</li>
<li>Panturrilhas</li>
</ul>
</div>

<div class="info-card">
<h2>Passo a passo</h2>
<ol>
<li>Sente-se na máquina de Leg Press com as costas apoiadas no encosto.</li>
<li>Posicione os pés na plataforma na largura dos ombros.</li>
<li>Destrave a máquina e flexione os joelhos lentamente.</li>
<li>Desça até formar aproximadamente um ângulo de 90° nos joelhos.</li>
<li>Empurre a plataforma estendendo as pernas sem travar os joelhos.</li>
</ol>
</div>

<div class="info-card">
<h2>Prescrição</h2>
<p><strong>Séries:</strong> 3</p>
<p><strong>Repetições:</strong> 10-12</p>
<p><strong>Descanso:</strong> 45s</p>
</div>

